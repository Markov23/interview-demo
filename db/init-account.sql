CREATE TABLE "ACCOUNT" (
    "ACCOUNT_NUMBER" VARCHAR(10) PRIMARY KEY,
    "ACCOUNT_TYPE" VARCHAR(50) CHECK ("ACCOUNT_TYPE" IN ('SAVINGS','CHECKING')),
    "INITIAL_BALANCE" NUMERIC(15,2) NOT NULL,
    "ACCOUNT_STATE" BOOLEAN NOT NULL DEFAULT TRUE,
    "CLIENT_ID" INT NOT NULL
);

CREATE TABLE "TRANSACTION" (
    "TRANSACTION_ID" BIGSERIAL PRIMARY KEY,
    "TRANSACTION_DATE" TIMESTAMP NOT NULL,
    "TRANSACTION_TYPE" VARCHAR(20) CHECK ("TRANSACTION_TYPE" IN ('DEPOSIT','REFUND','PAYMENT','TRANSFER')),
    "VALUE" NUMERIC(15,2) NOT NULL,
    "BALANCE" NUMERIC(15,2) NOT NULL,
    "ACCOUNT_NUMBER" VARCHAR(10) NOT NULL,
    CONSTRAINT fk_account FOREIGN KEY ("ACCOUNT_NUMBER") REFERENCES "ACCOUNT"("ACCOUNT_NUMBER")
);

